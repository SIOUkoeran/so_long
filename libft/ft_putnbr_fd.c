#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n < 0)
	{
		write(fd, "-", 1);
		if (n == -2147483648)
			write(fd, "2147483648", 10);
		else
		{
			n *= -1;
			ft_putnbr_fd(n, fd);
		}
	}
	else
	{
		if (n > 9)
		{
			ft_putnbr_fd(n / 10, fd);
		}
		n = 48 + n % 10;
		write(fd, &n, 1);
	}
}
